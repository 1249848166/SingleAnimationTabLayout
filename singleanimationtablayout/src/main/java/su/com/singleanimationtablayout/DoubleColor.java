package su.com.singleanimationtablayout;

public class DoubleColor {
    int fromColor;
    int toColor;
    public DoubleColor(int fromColor,int toColor){
        this.fromColor=fromColor;
        this.toColor=toColor;
    }

    public int getFromColor() {
        return fromColor;
    }

    public void setFromColor(int fromColor) {
        this.fromColor = fromColor;
    }

    public int getToColor() {
        return toColor;
    }

    public void setToColor(int toColor) {
        this.toColor = toColor;
    }
}
